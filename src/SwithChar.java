public class SwithChar {
    public static void main(String[] args) {
        char nilai='D';
        switch (nilai){
            case 'A':
                System.out.println("Mhs     : \"Trimkasih pak\"");
                System.out.println("Dosen     : \"Selamat ya !!\"");
                break;
            case 'B':
                System.out.println("Mhs     : \"Kenapa ulun kada dapat A,pak\"");
                System.out.println("Dosen   : \"kurang hoki\"");
                break;
            case 'C':
                System.out.println("Mhs     : \"Ulun turun full pak ai,Tugas full jua\"");
                System.out.println("Dosen   : \"Tapi dapat lah menjawab ujian\"");
                System.out.println("Mhs     : \"wkwkwk...\"");
                break;
            case 'D':
                System.out.println("Mhs     : \"Ulun turun full pak ai,Tugas full jua\"");
                System.out.println("Dosen   : \"Bujur jua kah\"");
                System.out.println("Mhs     : \"Memeriksa berkas\"");
                System.out.println("Dosen   : \"Senyum\"");
                System.out.println("Mhs     : \"Alhamdulillah\"");
                break;

        }
    }
}

